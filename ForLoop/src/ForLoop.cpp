#include <iostream>
using namespace std;

int main()
{

	for(int i=0; i<10; i++)
	{
		cout << "We have passed through the loop " << (i+1) << " times."<< endl;
	}

	cout << "\nFor loop finished!" << endl;

	return 0;
}
